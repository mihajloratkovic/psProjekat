    private void jbtnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnIzmeniActionPerformed
       int red = jtblRacuni.getSelectedRow();
    if (red < 0) {
        JOptionPane.showMessageDialog(this, "Izaberite račun za izmenu!");
        return;
    }
    
    Racun r = modelRacuni.vratiRacun(red);
    
    // Pošalji zahtev za učitavanje stavki ovog računa
    KlijentskiZahtevObjekat kzo = new KlijentskiZahtevObjekat();
    kzo.setOperacija(Operacije.VRATI_STAVKE_RACUNA);
    kzo.setObjekatOperacije(r.getIdRacun());
    
    try {
        Komunikacija.getInstanca().posalji(kzo);
        ServerskiOdgovorObjekat soo = Komunikacija.getInstanca().primi();
        
        if (soo.isUspesno()) {
            List<StavkaRacuna> stavke = (List<StavkaRacuna>) soo.getObjekatOperacija();
            r.setStavke(stavke); // Postavi stavke u račun
            
            // Otvori formu za izmenu sa svim stavkama
            FormaKreirajRacun forma = new FormaKreirajRacun(r);
            forma.setTitle("Izmena računa");
            forma.setVisible(true);
            
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Ne mogu da učitam stavke računa: " + soo.getPoruka(), 
                "Greška", JOptionPane.ERROR_MESSAGE);
        }
    } catch (InterruptedException ex) {
        Logger.getLogger(FormaPretragaRacuna.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jbtnIzmeniActionPerformed

