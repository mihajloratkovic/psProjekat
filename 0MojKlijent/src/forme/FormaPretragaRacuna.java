    private void jbtnPretrazi1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPretrazi1ActionPerformed
         Prodavac prodavac = null;
        Musterija musterija = null;
        String iznosStr = jtxtIznos.getText().trim();
        Double iznos = null;
        
        if (jComboProdavac.getSelectedIndex() > 0) {
            prodavac = (Prodavac) jComboProdavac.getSelectedItem();
        }
        
        if (jComboMusterija.getSelectedIndex() > 0) {
            musterija = (Musterija) jComboMusterija.getSelectedItem();
        }
        
        if (!iznosStr.isEmpty()) {
            try {
                iznos = Double.parseDouble(iznosStr);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Iznos mora biti broj!", "Greška", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        
        Racun r = new Racun();
        r.setProdavac(prodavac);
        r.setMusterija(musterija);
        r.setIznosSaPopustom(iznos != null ? iznos : 0);
        r.setKriterijumPretrage(iznosStr); 
        
        KlijentskiZahtevObjekat kzo = new KlijentskiZahtevObjekat();
        kzo.setOperacija(Operacije.PRETRAZI_RACUN);
        kzo.setObjekatOperacije(r);
        
        try {
            Komunikacija.getInstanca().posalji(kzo);
            ServerskiOdgovorObjekat soo = Komunikacija.getInstanca().primi();
            
            if (soo.isUspesno()) {
                List<Racun> racuni = (List<Racun>) soo.getObjekatOperacija();
                modelRacuni.postaviListu(racuni);
                if (racuni.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Nema računa po zadatim kriterijumima.", 
                        "Informacija", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Sistem je našao račune po zadatim kriterijumima.", 
                        "Uspešno!", JOptionPane.INFORMATION_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, soo.getPoruka(), "Greška!", JOptionPane.ERROR_MESSAGE);
            }
        } catch (InterruptedException ex) {
            Logger.getLogger(FormaPretragaRacuna.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbtnPretrazi1ActionPerformed

    

