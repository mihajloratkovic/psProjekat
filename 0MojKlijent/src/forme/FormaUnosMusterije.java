    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        
        String ime = jtxtIme.getText().trim();
        String prezime = jtxtPrezime.getText().trim();
        String email = jtxtEmail.getText().trim();
        String telefon = jtxtBrojTelefona.getText().trim();
        TipMusterije tip = (TipMusterije) jComboTip.getSelectedItem();

        Musterija musterija = izvrsiValidaciju(ime, prezime, email, telefon, tip);
        
        if (musterija != null) {
            KlijentskiZahtevObjekat kzo = new KlijentskiZahtevObjekat();
            kzo.setOperacija(Operacije.KREIRAJ_MUSTERIJU);
            kzo.setObjekatOperacije(musterija);
            
            try {
                Komunikacija.getInstanca().posalji(kzo);
                ServerskiOdgovorObjekat soo = Komunikacija.getInstanca().primi();
                
                if (soo.isUspesno()) {
                    JOptionPane.showMessageDialog(this, soo.getPoruka(), "Uspešno!", JOptionPane.INFORMATION_MESSAGE);
                    ocistiPolja();
                } else {
                    JOptionPane.showMessageDialog(this, soo.getPoruka(), "Greška!", JOptionPane.ERROR_MESSAGE);
                }
            } catch (InterruptedException ex) {
                Logger.getLogger(FormaUnosMusterije.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        

    }//GEN-LAST:event_jbtnSacuvajActionPerformed

