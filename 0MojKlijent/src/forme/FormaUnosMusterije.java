 private Musterija izvrsiValidaciju(String ime, String prezime, String email, String telefon, TipMusterije tip) {
        boolean ok = true;
        
        if (ime.isEmpty()) {
            jlblIme.setText("Obavezno polje!");
            jtxtIme.setBorder(new LineBorder(Color.RED, 2));
            ok = false;
        } else {
            jlblIme.setText("");
            jtxtIme.setBorder(new LineBorder(Color.GRAY, 1));
        }
        
        if (prezime.isEmpty()) {
            jlblPrezime.setText("Obavezno polje!");
            jtxtPrezime.setBorder(new LineBorder(Color.RED, 2));
            ok = false;
        } else {
            jlblPrezime.setText("");
            jtxtPrezime.setBorder(new LineBorder(Color.GRAY, 1));
        }
        
        if (email.isEmpty()) {
            jlblEmail.setText("Obavezno polje!");
            jtxtEmail.setBorder(new LineBorder(Color.RED, 2));
            ok = false;
        } else if (!email.contains("@")) {
            jlblEmail.setText("Email mora sadržati @!");
            jtxtEmail.setBorder(new LineBorder(Color.RED, 2));
            ok = false;
        } else {
            jlblEmail.setText("");
            jtxtEmail.setBorder(new LineBorder(Color.GRAY, 1));
        }
        
        if (telefon.isEmpty()) {
            jlblTelefon.setText("Obavezno polje!");
            jtxtBrojTelefona.setBorder(new LineBorder(Color.RED, 2));
            ok = false;
        } else {
            jlblTelefon.setText("");
            jtxtBrojTelefona.setBorder(new LineBorder(Color.GRAY, 1));
        }
        
        if (tip == null) {
            jlblTip.setText("Izaberite tip!");
            ok = false;
        } else {
            jlblTip.setText("");
        }
        
        if (ok) {
            Musterija m = new Musterija();
            m.setIme(ime);
            m.setPrezime(prezime);
            m.setEmail(email);
            m.setBrojTelefona(telefon);
            m.setTipMusterije(tip);
            return m;
        }
        return null;
    }

 private void popuniComboTipove() {
        jComboTip.removeAllItems();
        
        KlijentskiZahtevObjekat kzo = new KlijentskiZahtevObjekat();
        kzo.setOperacija(Operacije.VRATI_SVE_TIPOVE_MUSTERIJE);
        
        try {
            Komunikacija.getInstanca().posalji(kzo);
            ServerskiOdgovorObjekat soo = Komunikacija.getInstanca().primi();
            
            if (soo.isUspesno()) {
                List<TipMusterije> tipovi = (List<TipMusterije>) soo.getObjekatOperacija();
                for (TipMusterije t : tipovi) {
                    jComboTip.addItem(t);
                }
            }
        } catch (InterruptedException ex) {
            Logger.getLogger(FormaUnosMusterije.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void ocistiPolja() {
    jtxtIme.setText("");
    jtxtPrezime.setText("");
    jtxtEmail.setText("");
    jtxtBrojTelefona.setText("");
    jComboTip.setSelectedIndex(0);
    
    // Resetuj labele za greške
    jlblIme.setText("");
    jlblPrezime.setText("");
    jlblEmail.setText("");
    jlblTelefon.setText("");
    jlblTip.setText("");
    
    // Resetuj border-e
    jtxtIme.setBorder(new LineBorder(Color.GRAY, 1));
    jtxtPrezime.setBorder(new LineBorder(Color.GRAY, 1));
    jtxtEmail.setBorder(new LineBorder(Color.GRAY, 1));
    jtxtBrojTelefona.setBorder(new LineBorder(Color.GRAY, 1));
}

