    private void popuniComboProdavce() {
        jComboProdavac.removeAllItems();
        
        KlijentskiZahtevObjekat kzo = new KlijentskiZahtevObjekat();
        kzo.setOperacija(Operacije.VRATI_SVE_PRODAVCE);
        
        try {
            Komunikacija.getInstanca().posalji(kzo);
            ServerskiOdgovorObjekat soo = Komunikacija.getInstanca().primi();
            
            if (soo.isUspesno()) {
                List<Prodavac> prodavci = (List<Prodavac>) soo.getObjekatOperacija();
                for (Prodavac p : prodavci) {
                    jComboProdavac.addItem(p);
                }
                
                // Ako je prodavac već prijavljen, selektuj ga
                Prodavac prijavljeni = Kontroler.getInstanca().getPrijavljeniProdavac();
                if (prijavljeni != null) {
                    jComboProdavac.setSelectedItem(prijavljeni);
                }
            }
        } catch (InterruptedException ex) {
            Logger.getLogger(FormaKreirajRacun.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
   private void popuniComboMusterije() {
    jComboMusterija.removeAllItems();
    
    KlijentskiZahtevObjekat kzo = new KlijentskiZahtevObjekat();
    kzo.setOperacija(Operacije.VRATI_SVE_MUSTERIJE);
    
    try {
        Komunikacija.getInstanca().posalji(kzo);
        ServerskiOdgovorObjekat soo = Komunikacija.getInstanca().primi();
        
        if (soo.isUspesno()) {
            List<Musterija> musterije = (List<Musterija>) soo.getObjekatOperacija();
            for (Musterija m : musterije) {
                System.out.println("Musterija: " + m.getIme() + " " + m.getPrezime() 
                    + ", Tip: " + m.getTipMusterije().getNaziv() 
                    + ", Popust: " + m.getTipMusterije().getPopust());
                jComboMusterija.addItem(m);
            }
            
            jComboMusterija.addActionListener(new java.awt.event.ActionListener() {
                public void actionPerformed(java.awt.event.ActionEvent evt) {
                    izracunajUkupno();
                }
            });
        }
    } catch (InterruptedException ex) {
        Logger.getLogger(FormaKreirajRacun.class.getName()).log(Level.SEVERE, null, ex);
    }
}
    
    private void ucitajLjubimce() {
        KlijentskiZahtevObjekat kzo = new KlijentskiZahtevObjekat();
        kzo.setOperacija(Operacije.VRATI_SVE_LJUBIMCE);
        
        try {
            Komunikacija.getInstanca().posalji(kzo);
            ServerskiOdgovorObjekat soo = Komunikacija.getInstanca().primi();
            
            if (soo.isUspesno()) {
                listaSvihLjubimaca = (List<Ljubimac>) soo.getObjekatOperacija();
                modelLjubimci.postaviListu(listaSvihLjubimaca);
            }
        } catch (InterruptedException ex) {
            Logger.getLogger(FormaKreirajRacun.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void pretraziLjubimce() {
        String kriterijum = jtxtPretraga.getText().trim();
        
        if (kriterijum.isEmpty()) {
            modelLjubimci.postaviListu(listaSvihLjubimaca);
            return;
        }
        
        List<Ljubimac> filtrirani = new ArrayList<>();
        for (Ljubimac l : listaSvihLjubimaca) {
            if (l.getNaziv().toLowerCase().contains(kriterijum.toLowerCase()) ||
                l.getKategorija().toLowerCase().contains(kriterijum.toLowerCase())) {
                filtrirani.add(l);
            }
        }
        modelLjubimci.postaviListu(filtrirani);
    }
    
    private void izracunajUkupno() {
    double ukupno = modelStavke.getUkupanIznos();
    jtxtUkupno.setText(String.format("%.2f", ukupno));
    
    if (jComboMusterija.getSelectedItem() != null) {
        Musterija m = (Musterija) jComboMusterija.getSelectedItem();
        double popust = m.getTipMusterije().getPopust();
        jtxtPopust.setText(String.format("%.1f", popust));
        
        double zaUplatu = ukupno * (1 - popust / 100);
        jtxtZaUplatu.setText(String.format("%.2f", zaUplatu));
    } else {
        jtxtPopust.setText("0.0");
        jtxtZaUplatu.setText(jtxtUkupno.getText());
    }
}
    
    private void popuniPodatkeZaIzmenu() {
    if (racunZaIzmenu == null) return;
    
    // Postavi vrednosti u combo box-ove
    jComboProdavac.setSelectedItem(racunZaIzmenu.getProdavac());
    jComboMusterija.setSelectedItem(racunZaIzmenu.getMusterija());
    
    // Postavi datum
    SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
    jtxtDatum.setText(sdf.format(racunZaIzmenu.getDatumIzdavanja()));
    
    // Postavi stavke u tabelu 
    List<StavkaRacuna> stavke = racunZaIzmenu.getStavke();
    if (stavke != null) {
        modelStavke.postaviListu(stavke);
    } else {
        modelStavke.clear();
    }
    
    // Izračunaj ukupno
    izracunajUkupno();
    
    // Promeni naslov i dugmad
    jLabelNaslov.setText("Izmena računa");
    jbtnSacuvaj.setText("Sačuvaj izmene");
}

