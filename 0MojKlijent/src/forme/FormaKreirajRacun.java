    private void jbtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnSacuvajActionPerformed
        if (jComboMusterija.getSelectedItem() == null) {
        JOptionPane.showMessageDialog(this, "Izaberite musteriju!");
        return;
    }
    
    if (modelStavke.getStavke().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Račun mora imati bar jednu stavku!");
        return;
    }
    
    Prodavac prodavac = (Prodavac) jComboProdavac.getSelectedItem();
    Musterija musterija = (Musterija) jComboMusterija.getSelectedItem();
    
    Racun racun;
    if (izmenaMod && racunZaIzmenu != null) {
        // Koristi postojeći račun za izmenu
        racun = racunZaIzmenu;
        racun.setProdavac(prodavac);
        racun.setMusterija(musterija);
    } else {
        // Kreiraj novi račun
        racun = new Racun();
        racun.setDatumIzdavanja(new Date());
        racun.setProdavac(prodavac);
        racun.setMusterija(musterija);
    }
    
    racun.setPopust(musterija.getTipMusterije().getPopust());
    racun.setUkupanIznos(modelStavke.getUkupanIznos());
    racun.setIznosSaPopustom(racun.getUkupanIznos() - (racun.getUkupanIznos() * racun.getPopust() / 100));
    racun.setStavke(modelStavke.getStavke());
    
    KlijentskiZahtevObjekat kzo = new KlijentskiZahtevObjekat();
    kzo.setOperacija(izmenaMod ? Operacije.PROMENI_RACUN : Operacije.KREIRAJ_RACUN);
    kzo.setObjekatOperacije(racun);
    
    try {
        Komunikacija.getInstanca().posalji(kzo);
        ServerskiOdgovorObjekat soo = Komunikacija.getInstanca().primi();
        
        if (soo.isUspesno()) {
            JOptionPane.showMessageDialog(this, soo.getPoruka(), "Uspešno!", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, soo.getPoruka(), "Greška!", JOptionPane.ERROR_MESSAGE);
        }
    } catch (InterruptedException ex) {
        Logger.getLogger(FormaKreirajRacun.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_jbtnSacuvajActionPerformed

