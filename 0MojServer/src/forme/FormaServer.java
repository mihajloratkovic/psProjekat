    private void jbtnZaustaviServerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnZaustaviServerActionPerformed
    int odgovor = JOptionPane.showConfirmDialog(this, 
         "Da li ste sigurni da želite da zaustavite server?",
         "Potvrda gašenja", 
         JOptionPane.YES_NO_OPTION);

     if (odgovor == JOptionPane.YES_OPTION) {
         try {
             if (ps != null) {
                 ps.zaustaviServer();
             }

             jlblPokreniServer.setText("SERVER NIJE POKRENUT!");
             jlblPokreniServer.setForeground(Color.RED);
             jbtnPokreniServer.setVisible(true);
             jbtnZaustaviServer.setVisible(false);

             // Obriši tabelu ulogovanih
             ModelTabeleProdavci model = (ModelTabeleProdavci) jtblUlogovaniKorisnici.getModel();
             model.postaviListu(new ArrayList<>());

             JOptionPane.showMessageDialog(this, "Server je uspešno zaustavljen.");

         } catch (Exception ex) {
             JOptionPane.showMessageDialog(this, "Greška pri gašenju servera: " + ex.getMessage());
         }
     }
    }//GEN-LAST:event_jbtnZaustaviServerActionPerformed

