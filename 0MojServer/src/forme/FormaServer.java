    private void srediTabelu(ArrayList<OpstiDomenskiObjekat> listaProdavaca) {
    ModelTabeleProdavci mtp = new ModelTabeleProdavci(listaProdavaca);
    jtblUlogovaniKorisnici.setModel(mtp);
    jtblUlogovaniKorisnici.setFont(new Font("Comic Sans MS", Font.PLAIN, 14));
    jtblUlogovaniKorisnici.getTableHeader().setFont(new Font("Comic Sans MS", Font.PLAIN, 14));
    
    
    jtblUlogovaniKorisnici.getColumn("ID").setMinWidth(100);
    jtblUlogovaniKorisnici.getColumn("Ime i prezime").setMinWidth(400);
    jtblUlogovaniKorisnici.getColumn("Korisničko ime").setMinWidth(100);
    jtblUlogovaniKorisnici.getColumn("Datum zaposlenja").setMinWidth(150);
       
    JTableHeader header = jtblUlogovaniKorisnici.getTableHeader();
    header.setPreferredSize(new Dimension(100, 35));
}

